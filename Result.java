    private void filterinSubject(){
        String subjectText = jTextField2.getText();

        if (subjectText.equals("")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid subject");
        return;
    }

        String query = "SELECT * FROM student WHERE subject LIKE ?";

        try (Connection con = DatabaseConnection.getCon();
            PreparedStatement pst = con.prepareStatement(query)) {

            pst.setString(1, subjectText);

            try (ResultSet rs = pst.executeQuery()) {
                jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    private void filterResultsonmarks() {
        String marksText = jTextField1.getText();

        int marks = 0;
        if (!marksText.equals("")) {
            try {
                marks = Integer.parseInt(marksText);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please enter a valid number for marks");
                return;
            }
        }

        String query = "SELECT * FROM student WHERE marks >= ?";

        try (Connection con = DatabaseConnection.getCon();
            PreparedStatement pst = con.prepareStatement(query)) {

            pst.setInt(1, marks);

            try (ResultSet rs = pst.executeQuery()) {
                jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }


    }                                       

